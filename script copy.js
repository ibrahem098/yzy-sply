let proudctsGridElemnt = document.querySelector(".proudctsGrid")
function makeProductGrid() {
    products.forEach((product, index) => {
        // set id
        product.id = index + 1;
        // set images
        if (product.sizes > 3) {
            product.images = [
                `/assets/images/front/${product.color}/${product.title}.jpg`,
                `/assets/images/back/${product.color}/${product.title}T.jpg"`,
                `/assets/images/back/${product.color}/${product.title}B.jpg"`];
        } else if (product.sizes == 1) {
            if (product.isPrinted) {
                product.images = [
                    `/assets/images/front/${product.color}/${product.title}.${product.color === "media" ? "png" : "jpg"}`]
            } else {
                product.images = [
                    `/assets/images/front/${product.color}/${product.title}0.${product.color === "media" ? "png" : "jpg"}`]
            }
        } else {
            if (product.isPrinted) {
                product.images = [
                    `/assets/images/front/${product.color}/${product.title}.${product.color === "media" ? "png" : "jpg"}`,
                    `/assets/images/back/${product.color}/${product.title}.${product.color === "media" ? "png" : "jpg"}`];
            } else {
                product.images = [
                    `/assets/images/front/${product.color}/${product.title}0.${product.color === "media" ? "png" : "jpg"}`,
                    `/assets/images/back/${product.color}/${product.title}0.${product.color === "media" ? "png" : "jpg"}`];
            }
        }
        // add each product
        proudctHTML = `<div class="productCard"><img src="${product.images[0]}"><div class="info"><div class="title"><span>${product.title}</span></div><div class="addButtonContiner"><button class="buttonClear">+</button></div></div><div class="sizeSelectContiner"><div class="head"><button class="helpButton buttonClear">?</button><span>Select Size</span><button class="closeButton buttonClear">></button></div><div class="sizeList"><button class="sizeSet buttonClear">1</button><button class="sizeSet buttonClear">2</button><button class="sizeSet buttonClear">3</button></div></div></div>
`;
        // proudctHTML = `<div class="productCard"><img  src="${product.images[0]}"><div class="info"><span>${product.title}</span><div class="addButtonContiner"><button class="buttonClear">+</button></div></div></div>`;
        proudctsGridElemnt.innerHTML += proudctHTML;
    })
}
